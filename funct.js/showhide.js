function Show(){
    var imagen = document.getElementsByClassName("imagen") [0];
    
    if(imagen.style.visibility == "hidden"){
        imagen.style.visibility = "visible";
    } else{
        imagen.style.visibility = "visible";
    }
}
function Hide(){
    var imagen = document.getElementsByClassName("imagen") [0];
    if(imagen.style.visibility == "visible"){
        imagen.style.visibility = "hidden";
    } else{
        imagen.style.visibility = "hidden";
    }
}